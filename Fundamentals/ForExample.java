class ForExample {
	public static void main(String[] args)	{
		int i;
		for(i = 0; i <= 100 ; i++){ // i : block scope 내에서만 사용가능
			System.out.println(i + " " + "KOSTA 187기 화이팅!");
		}//for

		System.out.println(i); // 컴파일 에러 (cannot find symbol)
		

		// 누계합 구하기 
		int sum = 0; 
		for(int j = 0; j <= 100; j++){
			sum += j;
			
		}
		
		System.out.println("1 ~ 100 합 : " + sum);

		// 정사각형 형태
		for(int j=0; j<5; j++){
			for(int k=0; k<5; k++){
				System.out.print("*");
			}
			System.out.println();
		}


		

	}//main
}//class
